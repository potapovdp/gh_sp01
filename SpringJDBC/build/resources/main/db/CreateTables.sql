CREATE TABLE USERS
(
    ID        VARCHAR(100) NOT NULL PRIMARY KEY,
    LOGIN     VARCHAR(250),
    PASSWORD  VARCHAR(250),
    HASH_PASS VARCHAR(250),
    ROLE_TYPE VARCHAR(250),
    UNIQUE (LOGIN),
    PRIMARY KEY (ID)
);

CREATE TABLE PROJECT (
                         ID VARCHAR(100) NOT NULL
    , NAME VARCHAR(100) NOT NULL
    , DESCRIPTION VARCHAR(250) NOT NULL
    , STATUS VARCHAR(100) NOT NULL
    , START_DATE DATE
    , END_DATE DATE
    , USER_ID VARCHAR(100) NOT NULL
    , UNIQUE (NAME)
    , PRIMARY KEY (ID)
    , CONSTRAINT FK_PROJECT FOREIGN KEY (USER_ID)
        REFERENCES USERS (ID)
);

CREATE TABLE TASK (
                      ID VARCHAR(100) NOT NULL
    , NAME VARCHAR(100) NOT NULL
    , DESCRIPTION VARCHAR(250)
    , STATUS VARCHAR(100) NOT NULL
    , START_DATE DATE
    , END_DATE DATE
    , PROJECT_ID VARCHAR(100) NOT NULL
    , UNIQUE (NAME)
    , PRIMARY KEY (ID)
    , CONSTRAINT FK_TASK FOREIGN KEY (PROJECT_ID)
        REFERENCES PROJECT (ID)
);

CREATE TABLE SESSION (
                         ID VARCHAR(100) NOT NULL
    , ACTIVE BIT
    , USER_ID VARCHAR(100) NOT NULL
    , PRIMARY KEY (ID)
    , CONSTRAINT FK_SESSION FOREIGN KEY (USER_ID)
        REFERENCES USERS (ID)
);
